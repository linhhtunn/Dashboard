import type { LocalizedString, MedicationCycle, Patient, PatientStatus } from "@/types";

function text(vi: string, en = vi): LocalizedString {
  return { vi, en };
}

function medication(
  name: string,
  dosage: string,
  schedule: string,
  nextDoseAt: string | null,
): MedicationCycle {
  return {
    medication: text(name),
    dosage,
    schedule: text(schedule, schedule),
    lastTakenAt: null,
    nextDoseAt,
  };
}

function buildPatient(input: {
  id: string;
  mrn: string;
  name: string;
  age: number;
  gender: Patient["gender"];
  status: PatientStatus;
  wardCode: string;
  wardLabel: string;
  departmentCode: string;
  departmentLabel: string;
  bed: string;
  underlyingConditionCodes: string[];
  medicationCycle?: MedicationCycle[];
  recentSymptomCodes: string[];
  lastUpdated: string;
}): Patient {
  return {
    id: input.id,
    mrn: input.mrn,
    name: input.name,
    age: input.age,
    gender: input.gender,
    status: input.status,
    wardCode: input.wardCode,
    wardLabel: text(input.wardLabel, input.wardLabel),
    departmentCode: input.departmentCode,
    departmentLabel: text(input.departmentLabel, input.departmentLabel),
    bed: input.bed,
    underlyingConditionCodes: input.underlyingConditionCodes,
    medicationCycle: input.medicationCycle ?? [],
    recentSymptomCodes: input.recentSymptomCodes,
    lastUpdated: input.lastUpdated,
  };
}

